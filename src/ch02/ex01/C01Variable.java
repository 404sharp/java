package ch02.ex01;

public class C01Variable {
	public static void main(String[] args) {
		// declare a variable then use the variable
		boolean bl = false; // declaration. 변수를 만들었다.
		// assign a constant to a variable
		bl = true;
//		byte bl; // single line comment
		/*
		 * multi-line comment
		 */
		byte b = 0;
		short s = 0;
		char c = 0; // 유니코드이기 때문에 숫자로 표현된다.
		int i = 0;
		long l = 0;
		float f = 0.0f;
		double d = 0.0;
		
		// 기본 타입은 아니지만 워낙에 많이 쓰이는 것.
		// 업무에서 스트링을 많이 쓴다.
		String str = ""; // empty string에는 \n이 들어가 있다.
		// String(캐릭터 배열)의 끝에는 끝을 표시하기 위해 \n이 들어가 있다.
		
		int a;
		a = 0; // initialization
	}
}
